        private static void CreateAndAssignTags(GTDoroContext context, List<Task> tasks)
        {
            var tagHome = new Tag { Code = "home", Name = "Home", IsFixed = true, };
            var tagComputer = new Tag { Code = "computer", Name = "Computer", IsFixed = true, };
            context.Tags.Add(tagHome);
            context.Tags.Add(tagComputer);
            context.SaveChanges();

            tasks.Where(t => t.ID % 3 == 0).ToList().ForEach(t => t.Tags.Add(tagHome));
            tasks.Where(t => t.ID % 2 == 0).ToList().ForEach(t => t.Tags.Add(tagComputer));
            context.SaveChanges();
        }

